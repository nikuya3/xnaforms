namespace XnaForms.Input
{
    /// <summary>
    /// Specifies the buttons of a mouse input device.
    /// </summary>
    public enum MouseButton
    {
        /// <summary>
        /// Indicates the left button of a mouse input device.
        /// </summary>
        Left,

        /// <summary>
        /// Indicates the middle button of a mouse input device.
        /// </summary>
        Middle,

        /// <summary>
        /// Indicates no button of a mouse input device.
        /// </summary>
        None,

        /// <summary>
        /// Indicates the right button of a mouse input device.
        /// </summary>
        Right,

        /// <summary>
        /// Indicates the first X button of a mouse input device.
        /// </summary>
        XButton1,

        /// <summary>
        /// Indicates the second X button of a mouse input device.
        /// </summary>
        XButton2
    }
}
