namespace XnaForms
{
    /// <summary>
    /// Indicates the type of a <see cref="Style"/>.
    /// </summary>
    public enum StyleType
    {
        /// <summary>
        /// Indicates a <see cref="Style"/> used when an action is initiated.
        /// </summary>
        Action,

        /// <summary>
        /// Indicates a <see cref="Style"/> used when its implementer is activated.
        /// </summary>
        Active,

        /// <summary>
        /// Indicates a <see cref="Style"/> used for the default appearance.
        /// </summary>
        Default
    }
}
